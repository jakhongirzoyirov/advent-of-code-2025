    private static int day1(String inputShapes, String inputRegions) {
        String[] sh1 = inputShapes.split("\n\n");
        List<Shape> shapes = new ArrayList<>();

        for (String sh : sh1) {
            String[] shh = sh.split("\n");
            char charsh[][] = new char[3][3];
            for (int i = 0; i < charsh.length; i++) {
                charsh[i] = shh[i+1].toCharArray();
            }
            shapes.add(new Shape(charsh));
        }
        int result = 0;
        String[] regions = inputRegions.split("\n");

        for (String region : regions) {
            int W = Integer.parseInt(region.substring(0, region.indexOf('x')));
            int H = Integer.parseInt(region.substring(region.indexOf('x') + 1, region.indexOf(':')));
            char[][] grid = new char[H][W];
            for (char[] row : grid) Arrays.fill(row, '.');
            String[] fff = region.substring(region.indexOf(':') + 2).split(" ");
            int freqs[] = new int[fff.length];
            for (int i = 0; i < fff.length; i++) {
                freqs[i] = Integer.parseInt(fff[i]);
            }

            boolean ok = fitAll(grid, shapes, freqs);
            if (ok) result++;
        }
        return result;
    }

