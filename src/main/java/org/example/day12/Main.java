    static class Shape {
        char[][] m; // 3x3

        Shape(char[][] m) { this.m = m; }

        List<char[][]> rotations() {
            List<char[][]> list = new ArrayList<>();
            char[][] cur = m;
            for (int i = 0; i < 4; i++) {
                list.add(cur);
                cur = rotate(cur);
            }
            return list;
        }

        private char[][] rotate(char[][] a) {
            char[][] r = new char[3][3];
            for (int i = 0; i < 3; i++)
                for (int j = 0; j < 3; j++)
                    r[j][2 - i] = a[i][j];
            return r;
        }
    }

    // ---------------- Grid operations ----------------

    static boolean canPlace(char[][] grid, char[][] s, int r, int c) {
        int H = grid.length, W = grid[0].length;

        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) {
                if (s[i][j] == '#') {
                    int rr = r + i, cc = c + j;
                    if (rr < 0 || rr >= H || cc < 0 || cc >= W)
                        return false;
                    if (grid[rr][cc] == '#')
                        return false;
                }
            }
        }
        return true;
    }

    static void place(char[][] grid, char[][] s, int r, int c, char fill) {
        int H = grid.length, W = grid[0].length;

        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) {
                if (s[i][j] == '#') {
                    int rr = r + i, cc = c + j;
                    if (rr >= 0 && rr < H && cc >= 0 && cc < W)
                        grid[rr][cc] = fill;
                }
            }
        }
    }

    public static boolean fitAll(char[][] grid, List<Shape> shapes, int[] freq) {
        return solve(0, grid, shapes, freq);
    }

    private static boolean solve(int idx, char[][] grid, List<Shape> shapes, int[] freqs) {
        if (idx == shapes.size() || freqs[idx] == 0)
            return true;

        Shape sh = shapes.get(idx);
        freqs[idx]--;
        List<char[][]> rots = sh.rotations();

        int H = grid.length, W = grid[0].length;

        for (char[][] r1 : rots) {
            for (int i1 = -2; i1 < H; i1++) {
                for (int j1 = -2; j1 < W; j1++) {

                    if (!canPlace(grid, r1, i1, j1)) return false;

                    place(grid, r1, i1, j1, '#');

                    for (char[][] r2 : rots) {
                        for (int i2 = -2; i2 < H; i2++) {
                            for (int j2 = -2; j2 < W; j2++) {

                                if (!canPlace(grid, r2, i2, j2)) return false;

                                place(grid, r2, i2, j2, '#');

                                if (solve(idx + 1, grid, shapes, freqs)) return true;

                                place(grid, r2, i2, j2, '.'); // undo 2nd
                            }
                        }
                    }

                    place(grid, r1, i1, j1, '.'); // undo 1st
                }
            }
        }

        return false;
    }

