    private static long day1(int[][] grid) {
        long maxArea = 0;
        for (int i = 0; i < grid.length; i++) {
            for (int j = i + 1; j < grid.length; j++) {
                long area = (long) (Math.abs(grid[i][0] - grid[j][0]) + 1) * (Math.abs(grid[i][1] - grid[j][1]) + 1);
                maxArea = Math.max(maxArea, area);
            }
        }
        return maxArea;
    }

